class Student{
  private String name;
  private int funds;
  private char gender;

  public Student (String inputName, int funds, char gender){
    this.name = inputName;
    this.funds = funds;
    this.gender = gender;
  }

  public int checkFunds() {
    return funds = this.funds;
  }

  public char checkGender() {
    return gender = this.gender;
  }

  
}



